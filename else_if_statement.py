# ELIF = else if statement

nama = input("nama kamu siapa? ")

if nama == "arfan": # kondisi 1
    print("hai apa kabar") # aksi true 1

elif nama == "kaco": # kondisi 2
    print("hai kaco apa kabar") # aksi true 2
elif nama == "cicci": # kondisi ke 3
    print("hai cicci apa kabar") # aksi true 3
else:
    print("kamu salah masuk nama!") # aksi false
print("terimakasi")
